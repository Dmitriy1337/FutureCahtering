
public class DishInDescr {

	private String name;
	private String number;
	private String price;
	
	
	
	public DishInDescr(String name,String number,String price){
		this.name = name;
		this.number = number;
		this.price = price;
		
		
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public String getNumber() {
		return number;
	}



	public void setNumber(String number) {
		this.number = number;
	}



	public String getPrice() {
		return price;
	}



	public void setPrice(String price) {
		this.price = price;
	}
	
	
	
	
}
